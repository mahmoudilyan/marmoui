async function main() {
	for (const a of articles) {
		await client.createOrReplace({
			_id: a.id,
			_type: 'article',
			title: a.title,
			slug: { _type: 'slug', current: a.slug },
			excerpt: a.excerpt,
			publishedAt: new Date().toISOString(),
			body: a.body,
			seoTitle: a.seoTitle,
			seoDescription: a.seoDescription,
		});
		console.log(`✅ ${a.title} -> /blog/${a.slug}`);
	}
}

main().catch(e => {
	console.error(e);
	process.exit(1);
});
