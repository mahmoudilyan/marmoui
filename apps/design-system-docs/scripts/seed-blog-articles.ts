let k = 0;
const key = () => `k${k++}`;
type Seg = string | { t: string; href: string };

function rich(style: string, segments: Seg[]) {
	const markDefs: { _type: string; _key: string; href: string }[] = [];
	const children = segments.map(seg => {
		if (typeof seg === 'string') return { _type: 'span', _key: key(), text: seg, marks: [] as string[] };
		const mk = key();
		markDefs.push({ _type: 'link', _key: mk, href: seg.href });
		return { _type: 'span', _key: key(), text: seg.t, marks: [mk] };
	});
	return { _type: 'block', _key: key(), style, markDefs, children };
}
const p = (...s: Seg[]) => rich('normal', s);
const h2 = (t: string) => rich('h2', [t]);
function bullets(items: Seg[][]) {
	return items.map(segs => ({ ...rich('normal', segs), listItem: 'bullet', level: 1 }));
}

